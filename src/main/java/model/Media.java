package model;

/**
 *
 * @author HECTOR
 */
public class Media {
    private String url;
    private String type;

    public Media() {
        
    }

    public Media(String url) {
        this.url = url;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    @Override
    public String toString() {
        return "Media: " + "\n\tUrl: " + url;
    }
    
    
    
}
